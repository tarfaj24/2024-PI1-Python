cislodna = int(input("Zadaj číso dňa: "))
if cislodna == 1:
    print("pondelok")
elif cislodna == 2:
    print("utorok")
elif cislodna == 3:
    print("streda")
elif cislodna == 4:
    print("štvrtok")
elif cislodna == 5:
    print("piatok")
elif cislodna == 6:
    print("sobota")
elif cislodna == 7:
    print("nedeľa")
else:
    print("Taký deň neexistuje.")