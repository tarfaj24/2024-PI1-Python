a = int(input("zadaj číslo a:"))  # input je príkazom vstupu. Vypíše na obrazovku text v zátvorke a to čo uživateľ napíše na klávesici uloží do premennej a
b = int(input("zadaj číslo b:")) #input vracia vždy výsledok vo formáte reťazca (postupnosť znakov resp. text), funkcia int()pretypuje reťazec na !celé! číslo
sucet = a + b
print("Súčet je:", sucet)
sucin = a * b
print("Súčin je:", sucin)
podiel = a / b
print("Podiel je:", podiel)
rozdiel = a - b
print("Rozdiel je:", rozdiel)