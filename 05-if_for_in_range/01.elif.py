cislo_dna = int(input("Zadaj číslo dňa: "))
if cislo_dna == 1:
    print("Pondelok")
elif cislo_dna == 2:
    print("Utorok")
elif cislo_dna == 3:
    print("Streda")
elif cislo_dna == 4:
    print("Štvrtok")
elif cislo_dna == 5:
    print("Piatok")
elif cislo_dna == 6:
    print("Sobota")
elif cislo_dna == 7:
    print("Nedeľa")
else:
    print("Taký deň neexistuje!")